#include "libft.h"

void	ft_strncpy(char *dst, char *src, int n)
{
	int	i;

	i = 0;
	if (!dst || !src)
		return ;
	while (src[i] != '\0' && i < n)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
}
