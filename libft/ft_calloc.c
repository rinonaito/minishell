#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*allocated;

	if (count == 0 || size == 0)
	{
		count = 1;
		size = 1;
	}
	if (count > SIZE_MAX / size)
		return (NULL);
	allocated = (void *)malloc(count * size);
	if (allocated == NULL)
		return (NULL);
	ft_bzero(allocated, count * size);
	return (allocated);
}
